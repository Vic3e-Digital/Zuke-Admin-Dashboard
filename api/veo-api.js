// api/veo-api.js
const express = require('express');
const router = express.Router();
const { getDatabase } = require('../lib/mongodb');
const { ObjectId } = require('mongodb');
const axios = require('axios');
const { GoogleGenAI } = require('@google/genai');

const GEMINI_API_KEY = process.env.GEMINI_API_KEY;
const N8N_WALLET_WEBHOOK = process.env.N8N_WALLET_WEBHOOK_URL;

// Initialize Google Generative AI client
const ai = new GoogleGenAI({
  apiKey: GEMINI_API_KEY
});

// Pricing
const PRICING = {
  generation: 20  // R20.00 per video generation
};

// ========== GENERATE VIDEO ROUTE ==========
router.post('/generate', async (req, res) => {
  console.log('üé¨ ========== VEO GENERATION REQUEST ==========');
  console.log('üì¶ Request Body:', JSON.stringify(req.body, null, 2));
  
  try {
    const {
      businessId,
      userEmail,
      model,
      prompt,
      negativePrompt,
      image,
      config
    } = req.body;

    // Validate required fields
    if (!businessId || !userEmail || !model || !prompt) {
      return res.status(400).json({
        success: false,
        error: 'Missing required fields',
        received: { businessId, userEmail, model, prompt: !!prompt }
      });
    }

    console.log('‚úÖ Validation passed');

    // Get business info
    const db = await getDatabase();
    const business = await db.collection('store_submissions').findOne({
      _id: new ObjectId(businessId)
    });

    if (!business) {
      return res.status(404).json({
        success: false,
        error: 'Business not found'
      });
    }

    console.log('‚úÖ Business found:', business.store_info?.name);

    // Generate request ID
    const requestId = `veo_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;

    // ‚úÖ WALLET CHECK AND DEDUCTION
    const walletPayload = {
      userEmail,
      businessId,
      cost: PRICING.generation,
      requestId,
      description: 'Veo AI video generation',
      metadata: {
        model,
        duration: config?.durationSeconds,
        resolution: config?.resolution
      }
    };

    console.log('üí∞ Checking wallet...');

    const walletResponse = await axios.post(N8N_WALLET_WEBHOOK, walletPayload, {
      headers: { 'Content-Type': 'application/json' },
      timeout: 30000,
      validateStatus: (status) => status >= 200 && status < 300 || status === 402
    });

    const walletResult = walletResponse.data;

    if (walletResponse.status === 402 || !walletResult.success) {
      console.log('‚ö†Ô∏è Insufficient funds');
      return res.status(402).json({
        success: false,
        error: 'Insufficient funds',
        current_balance: walletResult.current_balance,
        required: PRICING.generation,
        formatted_balance: walletResult.formatted_balance,
        formatted_required: `R${PRICING.generation.toFixed(2)}`
      });
    }

    console.log('‚úÖ Wallet deduction successful:', walletResult.formatted_balance);

    // ‚úÖ CALL GEMINI VEO API USING SDK
    console.log('üé¨ Calling Gemini Veo API with SDK...');
    
    // Build the request object
    const generateRequest = {
      model: model,
      prompt: prompt
    };

    // Add config if provided
    if (config) {
      generateRequest.config = {
        aspectRatio: config.aspectRatio || "16:9",
        resolution: config.resolution || "720p",
        durationSeconds: parseInt(config.durationSeconds) || 8,
        numberOfVideos: config.numberOfVideos || 1
      };
    }

    // Add negative prompt if provided
    if (negativePrompt && negativePrompt.trim()) {
      generateRequest.negativePrompt = negativePrompt.trim();
    }

    // Add image if provided (for image-to-video)
    if (image) {
      generateRequest.image = {
        imageBytes: image.imageBytes,
        mimeType: image.mimeType || 'image/png'
      };
    }

    console.log('üì¶ SDK Request:', JSON.stringify(generateRequest, null, 2));

    // ‚úÖ USE SDK TO GENERATE VIDEO
    const operation = await ai.models.generateVideos(generateRequest);

    const operationName = operation.name;

    if (!operationName) {
      console.error('‚ùå No operation name in response');
      throw new Error('No operation name returned from Gemini API');
    }

    console.log('‚úÖ Video generation started:', operationName);

    // ‚úÖ STORE GENERATION REQUEST IN MONGODB
    await db.collection('veo_generations').insertOne({
      businessId: new ObjectId(businessId),
      userEmail,
      model,
      prompt,
      negativePrompt: negativePrompt || null,
      config,
      operationName,
      cost: PRICING.generation,
      charged: true,
      status: 'processing',
      requestId,
      created_at: new Date()
    });

    // ‚úÖ RETURN SUCCESS WITH OPERATION NAME
    res.json({
      success: true,
      operationName: operationName,
      message: 'Video generation started',
      charged: true,
      formatted_cost: `R${PRICING.generation.toFixed(2)}`,
      new_balance: walletResult.new_balance,
      formatted_balance: walletResult.formatted_balance
    });

  } catch (error) {
    console.error('‚ùå Generation error:', error);
    
    if (error.response) {
      console.error('‚ùå Error response:', error.response.status);
      console.error('‚ùå Error data:', JSON.stringify(error.response.data, null, 2));
      return res.status(500).json({
        success: false,
        error: `Gemini API error: ${error.response.status}`,
        details: error.response.data
      });
    }
    
    res.status(500).json({
      success: false,
      error: error.message
    });
  }
});


// ========== CHECK VIDEO STATUS ROUTE (UPDATED LOGGING) ==========
router.post('/check-status', async (req, res) => {
    console.log('üîç ========== CHECKING VIDEO STATUS ==========');
    
    try {
      const { operationName } = req.body;
  
      if (!operationName) {
        console.error('‚ùå Missing operationName');
        return res.status(400).json({
          success: false,
          error: 'Missing operationName'
        });
      }
  
      console.log('üìç Operation Name:', operationName);
      console.log('üîÑ Calling Google REST API directly...');
  
      const apiUrl = `https://generativelanguage.googleapis.com/v1alpha/${operationName}`;
      
      const response = await axios.get(apiUrl, {
        headers: {
          'x-goog-api-key': GEMINI_API_KEY
        },
        timeout: 30000
      });
  
      const operation = response.data;
  
      console.log('‚úÖ REST API response received');
      console.log('üìä Operation.done:', operation.done);
      
      if (operation.done) {
        console.log('\nüéâ ========== VIDEO GENERATION COMPLETE! ==========');
        
        if (operation.error) {
          console.error('‚ùå Operation has error:', JSON.stringify(operation.error, null, 2));
        }
        
        if (operation.response) {
          console.log('üì¶ operation.response exists ‚úì');
          console.log('üì¶ operation.response.@type:', operation.response['@type']);
          
          // ‚úÖ Check the ACTUAL path
          const videoResponse = operation.response.generateVideoResponse;
          
          if (videoResponse) {
            console.log('‚úÖ operation.response.generateVideoResponse exists ‚úì');
            console.log('üì¶ generateVideoResponse keys:', Object.keys(videoResponse));
            
            const generatedSamples = videoResponse.generatedSamples;
            
            if (generatedSamples) {
              console.log('‚úÖ generatedSamples exists ‚úì');
              console.log('üìä generatedSamples.length:', generatedSamples.length);
              
              if (generatedSamples.length > 0) {
                console.log('\nüìπ ========== FIRST VIDEO SAMPLE ==========');
                const firstSample = generatedSamples[0];
                console.log('üì¶ Sample keys:', Object.keys(firstSample));
                console.log('üìπ Video URI:', firstSample.video?.uri);
                console.log('üìπ Full sample:', JSON.stringify(firstSample, null, 2));
              }
            } else {
              console.log('‚ö†Ô∏è No generatedSamples');
            }
          } else {
            console.log('‚ö†Ô∏è No generateVideoResponse');
            console.log('üì¶ Response keys:', Object.keys(operation.response));
          }
        }
        
        console.log('========================================\n');
        
        // Update status in database
        const db = await getDatabase();
        await db.collection('veo_generations').updateOne(
          { operationName: operationName },
          { 
            $set: { 
              status: operation.error ? 'failed' : 'completed',
              completed_at: new Date(),
              fullOperation: operation
            }
          }
        );
      } else {
        console.log('‚è≥ Video still processing...');
      }
  
      // Return the raw REST API response
      res.json(operation);
  
    } catch (error) {
      console.error('‚ùå ========== STATUS CHECK ERROR ==========');
      console.error('‚ùå Error message:', error.message);
      
      if (error.response) {
        console.error('‚ùå HTTP Status:', error.response.status);
        console.error('‚ùå Response data:', JSON.stringify(error.response.data, null, 2));
        
        return res.status(error.response.status).json({
          success: false,
          error: error.response.data?.error?.message || error.message,
          details: error.response.data
        });
      }
      
      res.status(500).json({
        success: false,
        error: error.message
      });
    }
  });


// ========== DOWNLOAD VIDEO ROUTE (FIXED) ==========
router.post('/download', async (req, res) => {
  console.log('‚¨áÔ∏è ========== DOWNLOADING VIDEO ==========');
  
  try {
    const { videoFile } = req.body;

    if (!videoFile || !videoFile.uri) {
      return res.status(400).json({
        success: false,
        error: 'Missing video file URI'
      });
    }

    console.log('üìç Video file object:', JSON.stringify(videoFile, null, 2));

    // ‚úÖ FIXED: Use & instead of ? to append the API key
    // The URI already has ?alt=media, so we append with &
    const downloadUrl = `${videoFile.uri}&key=${GEMINI_API_KEY}`;
    console.log('üìç Download URL:', downloadUrl);
    
    const response = await axios.get(downloadUrl, {
      responseType: 'arraybuffer',
      timeout: 120000,  // 2 minutes for large videos
      maxContentLength: 100 * 1024 * 1024,  // 100MB max
      headers: {
        'Accept': 'video/mp4'
      }
    });

    console.log('‚úÖ Video downloaded successfully');
    console.log('üìä Size:', response.data.length, 'bytes');
    console.log('üìä Content-Type:', response.headers['content-type']);

    res.set('Content-Type', 'video/mp4');
    res.set('Content-Length', response.data.length);
    res.send(Buffer.from(response.data));

  } catch (error) {
    console.error('‚ùå ========== DOWNLOAD ERROR ==========');
    console.error('‚ùå Error message:', error.message);
    
    if (error.response) {
      console.error('‚ùå Response status:', error.response.status);
      console.error('‚ùå Response data:', error.response.data?.toString());
    }
    
    res.status(500).json({
      success: false,
      error: error.message
    });
  }
});

// ========== GET PRICING ==========
router.get('/pricing', (req, res) => {
  res.json({
    success: true,
    pricing: {
      generation: {
        amount: PRICING.generation,
        formatted: `R${PRICING.generation.toFixed(2)}`
      }
    }
  });
});

module.exports = router;